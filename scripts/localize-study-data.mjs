function examplesFor(character, words) {
  return words
    .filter((word) => String(word.japanese || "").includes(character))
    .slice(0, 3)
    .map((word) => ({
      word: word.japanese,
      reading: word.reading || "",
      meaningKo: word.meaningKo || ""
    }));
}

function groupByLevel(items) {
  return items.reduce((groups, item) => {
    const level = item.level || "Other";
    groups[level] ||= [];
    groups[level].push(item);
    return groups;
  }, {});
}

function extractUsedKanji(value) {
  return Array.from(new Set(String(value || "").match(/[\u3400-\u4dbf\u4e00-\u9fff\uf900-\ufaff]/gu) || [])).join(" ");
}

