function cleanEnglish(value) {
  return String(value || "")
    .replace(/\s+/g, " ")
    .replace(/\d+\./g, "")
    .replace(/\([^)]*\)/g, "")
    .replace(/,+/g, ",")
    .trim();
}

function splitGloss(value) {
  return unique(
    cleanEnglish(value)
      .split(/\s*\/\s*|\s*,\s*/)
      .map((part) => part.replace(/[.;:]+$/g, "").trim())
      .filter((part) => part && part !== "etc.)" && part !== "etc")
  );
}

function translateUnknown(part) {
  for (const [pattern, replacement] of fallbackHints) {
    if (pattern.test(part)) {
      return part.replace(pattern, replacement).replace(/[A-Za-z]+/g, (token) => tokenGloss.get(token.toLowerCase()) || "");
    }
  }

  const converted = part
    .toLowerCase()
    .replace(/[-/]/g, " ")
    .split(/\s+/)
    .map((token) => tokenGloss.get(token) || "")
    .filter(Boolean)
    .join(" ");

  return converted || "뜻 보완 필요";
}

function translateGloss(value) {
  const translated = splitGloss(value)
    .map((part) => {
      const key = part.toLowerCase();
      return exactGloss.get(key) || translateUnknown(part);
    })
    .map((part) =>
      part
        .replace(/\s+/g, " ")
        .replace(/하다하다/g, "하다")
        .replace(/이다이다/g, "이다")
        .trim()
    )
    .filter((part) => part && part !== "뜻 보완 필요");

  return unique(translated).slice(0, 6).join(", ") || "관련 표현";
}

